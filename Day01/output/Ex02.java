package output;

// 주석 : 코드에서 해석 (= 실행)하지 않는 부분
// - 슬래시 두개로 표현되고 한 줄 끝가지 처리 된다

public class Ex02 {

	public static void main(String[] args) {
		// System : 표준 입출력을 제공하는 클래스
		// out : 표준 출력 객체
		
		System.out.println("Hello Java");
		System.out.println("코드는 위에서 아래로 순차적으로 수행");
		
		System.out.println("빈 줄과 주석은 실행하지 않는다");
		
	}

}
